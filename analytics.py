with tab1:
    st.markdown("### Macro Data Step Transformation Script")
    sas_code = """/* Macro array transformation to clean baseline patient demographics */
%macro clean_demographics(input_ds, output_ds);
    data &output_ds.;
        set &input_ds.;
        where age >= 18 and missing(patient_id) = 0;
        
        /* Standardize character inputs */
        gender = upcase(strip(gender));
        if gender not in ('M', 'F') then gender = 'UNKNOWN';
        
        /* Calculate baseline BMI metrics */
        if height > 0 then bmi = (weight / (height**2)) * 703;
        else bmi = .;
    run;
%mend clean_demographics;

%clean_demographics(work.raw_patients, work.analytics_ready);
proc print data=work.analytics_ready(obs=5) label;
    var patient_id age gender bmi;
run;"""
    st.code(sas_code, language="sas")

